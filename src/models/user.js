import mongoose from 'mongoose';
const { Schema, model } = mongoose;

const UserSchema = Schema({
    name: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String, 
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
    role: {
        type: String,
        required: true
    }
});

const Users = mongoose.model('Users', UserSchema);

export {Users};